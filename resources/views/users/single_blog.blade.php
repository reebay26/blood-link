    <div class="moto-widget-text-content moto-widget-text-editable"><p class="moto-text_normal">Media prices are falling, so advertising becomes more profitable. The combination of low prices on media and weak competition gives companies the opportunity to cheaply grab market share.</p></div>
</div><div data-widget-id="wid__image__5ab37e80d01f4" class="moto-widget moto-widget-image moto-widget_with-deferred-content moto-preset-default moto-align-center moto-spacing-top-small moto-spacing-right-auto moto-spacing-bottom-small moto-spacing-left-auto  " data-widget="image">
