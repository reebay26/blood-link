    <div class="moto-widget-text-content moto-widget-text-editable"><p class="moto-text_normal">Then came truly frightening times for marketing managers. How to respond? What is the optimal strategy? There are several rules of survival in the times of crisis.</p><p class="moto-text_normal">&nbsp;</p><p class="moto-text_normal">Do not panic. Most marketers assume that during the crisis consumers have sharply cut their spendings. In fact, consumer spendings rarely really fall, they simply grow more slowly, not at the pace of inflation.</p><p class="moto-text_normal">&nbsp;</p><p class="moto-text_normal">Cut the correct costs. To the right are the administrative costs and even reduction of volumes of manufacture. It is impossible to start saving on quality of a product or its promotion.</p><p class="moto-text_normal">&nbsp;</p><p class="moto-text_normal">Reduce of advertising costs inevitably will reduce your income. This is the easiest and fastest way to cut costs, but the reckoning is inevitable. Studies have shown that firms that reduce advertising costs during a recession typically experience 20-30% decline in sales and earnings over the next two years.</p><p class="moto-text_normal">&nbsp;</p><p class="moto-text_normal">Reduce of advertising costs inflicts long-term harm. By results of researches, advertising has a lasting effect on sales: it becomes obvious in up to five years after the campaign. Cutting advertising budgets is hurting business for the long term. PIMS analysis shows that companies that shorten the ads need much more time to exit the crisis than all the rest (when the economic situation begins to improve).</p></div>
